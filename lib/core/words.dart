int getPuzzleNumber() {
  final launch = DateTime(2026, 1, 1);
  final now = DateTime.now().toUtc().add(const Duration(hours: 3)); // Riyadh UTC+3
  final diff = now.difference(launch).inDays;
  return diff < 0 ? 1 : diff + 1;
}

